			<h1><img class="img1" width="400" height="300" src="Pictures\1.jpg" align="left" hspace="10"><ins>Описание</ins></h1>
			<div align="justify">
				<p>Любителям отдыха на природе советую попробовать шашлык, <em>замаринованный в вине</em>. 
				Результат вам должен понравиться. Винная кислота размягчает волокна мяса, делая его 
				нежным и мягким, а сухие вещества вина придают шашлыку неповторимый легкий аромат.</p>
		
				<p><b>Маринад</b> – это смесь приправ, растительных масел и натуральных продуктов, содержащих кислоты.
				Выдержанное в маринаде мясо приобретает мягкую консистенцию и аромат, обусловленный ингредиентами
				маринада. Для свинины, говядины и баранины маринады делают насыщенными, тогда как маринады для
				птицы и рыбы должны быть более деликатными и нежными. Чем жестче мясо, тем дольше нужно выдерживать
				его в маринаде. Соль класть в маринады не рекомендуется, так как гипертонический раствор просто
				«высосет» из мяса весь сок, и шашлык получится сухим. По этой же причине не следует делать маринад 
				и слишком «кислым».</p>
		
				<p><b>Маринада не должно быть слишком много.</b> Так как в процессе маринования мясо даст свой сок, количество
				жидкости еще увеличится – это нужно иметь в виду.</p>
		
				<p><b>Для приготовления шашлыков в вине оптимально подходит:</b> <em>свиное, говяжье или баранье мясо.</em>
				Курицу и рыбу лучше мариновать по другим рецептам. Нельзя замачивать мясо в алюминиевой или
				деревянной посуде, так как при контакте с металлом вино окисляется, а дерево быстро впитывает
				в себя маринад.</p>
		
				<p>Подходит любое <ins><b>сухое</b> красное или белое вино</ins>. От использования сладких, полусладких и крепленых 
				видов лучше отказаться, поскольку они плохо размягчают мясо и не дают нужного аромата. Нет смысла 
				покупать дорогой напиток, можно обойтись даже молодым домашним виноградным вином, которое прекрасно 
				справляется со своей задачей.</p>
		
				<p>Винный маринад придает мясу нежный аромат и делает его мягче. <em>Однако не всем нравится винный привкус.</em> 
				Тем не менее, для желающих попробовать замариновать мясо в вине рекомендуется на те же два килограмма 
				говядины или свинины брать литр сухого красного вина, три луковицы средних размеров, побольше черного 
				перца и базилика, можно свежего.</p> 
		
				<p><b>Если возьмете белое вино, ваш маринад подойдет и для индейки.<b></p>
